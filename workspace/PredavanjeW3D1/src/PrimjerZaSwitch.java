
public class PrimjerZaSwitch {

	public static void main(String[] args) {
		
		int a = 2;
		
		switch (a){
		
		case 1:
			System.out.println("One");
			System.out.println("a je jedan");
			break;
		case 2:
			System.out.println("Two");
			
		case 3:
			System.out.println("Three");
			
		default :
			System.out.println("Default");
		}

	}

}
